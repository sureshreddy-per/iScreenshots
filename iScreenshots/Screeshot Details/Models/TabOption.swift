import Foundation

/// An enumeration representing options available for interacting with an item, typically used in a tab below detailed page.
enum TabOption: String {
    case share = "Share"   // Option to share the item.
    case info = "Info"     // Option to view detailed information about the item.
    case delete = "Delete" // Option to delete the item.
}
